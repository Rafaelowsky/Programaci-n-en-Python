numero1 = 5
numero2 = 10

#Los operadores relacionales son los siguientes:

# == Igual que
print(numero1 == numero2) #False

# != Distinto que
print(numero1 != numero2) #True

# < Menor que
print(numero1 < numero2) #True

# > Mayor que
print(numero1 > numero2) #False

# <= Menor o igual que
print(numero1 <= numero2) #True

# >= Mayor o igual que
print(numero1 >= numero2) #False
