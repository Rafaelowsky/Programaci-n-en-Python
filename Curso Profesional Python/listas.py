#Para poder declarar una lista en Python se hace de la siguiente manera:

lista = [] #Lista vacia

#Las listas pueden guardar varios tipos de datos
#          0      1     2     3       4
#          -5    -4    -3    -2      -1
lista = ['Hola', True, 4.5, False, 'Mundo']

"""En Python los indices empiezan desde 0
Para poder acceder a un elemento de la lista se hace de la siguiente manera: """
print(lista[0]) #Hola
print(lista[-5]) #Hola

#Para actualizar los elementos de una lista

lista[0] = 'Adios'
print(lista[0]) #Adios

